package com.ougnt.period_manager.activity.extra;

public class InstructionComponent {

    public InstructionComponent(int sx, int sy, int w, int h, String t) {
        startX = sx;
        startY = sy;
        width = w;
        height = h;
        text = t;
    }

    public int startX;
    public int startY;
    public int width;
    public int height;
    public String text;
}
